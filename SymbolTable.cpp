int NrSymbols = sizeof(SymbolTable)/sizeof(SSymbol);

const char *Lookup(uint16_t fAddress)
{
  for(int i = 0; i < NrSymbols; i++)
  {
    const SSymbol *symbol = &(SymbolTable[i]);

    if(symbol->Value == fAddress)
      return symbol->Name;
  }
  return NULL;
}

int Lookup(const char *fSymbol)
{
  for(int i = 0; i < NrSymbols; i++)
  {
    const SSymbol *symbol = &(SymbolTable[i]);

    if(strcasecmp(fSymbol, symbol->Name) == 0)
      return symbol->Value;
  }
  return -1;
}
